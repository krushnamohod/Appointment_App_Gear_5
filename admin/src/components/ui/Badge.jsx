import { cn } from "@/lib/utils";
import { cva } from "class-variance-authority";

/**
 * @intent Badge component for status labels
 * @param {object} props - Badge props
 * @param {string} props.variant - Badge variant: default|secondary|outline|destructive|success
 */
const badgeVariants = cva(
    "inline-flex items-center rounded-md border px-2.5 py-0.5 text-xs font-semibold transition-colors focus:outline-none focus:ring-2 focus:ring-ring focus:ring-offset-2",
    {
        variants: {
            variant: {
                default: "border-transparent bg-primary text-primary-foreground shadow",
                secondary: "border-transparent bg-secondary text-secondary-foreground",
                outline: "text-foreground",
                destructive: "border-transparent bg-destructive text-destructive-foreground shadow",
                success: "border-transparent bg-emerald-500 text-white shadow",
            },
        },
        defaultVariants: {
            variant: "default",
        },
    }
);

function Badge({ className, variant, ...props }) {
    return <div className={cn(badgeVariants({ variant }), className)} {...props} />;
}

export { Badge, badgeVariants };

